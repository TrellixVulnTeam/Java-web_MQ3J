package DTO;

/**
 *
 * @author MacOS
 */
public class CartChild {

    private Book book;
    private int quantity;

    public CartChild() {

    }

    public CartChild(Book book, int quantity) {
        this.book = book;
        this.quantity = quantity;
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

}
